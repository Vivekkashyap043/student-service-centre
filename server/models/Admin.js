import mongoose from "mongoose";

const Schema = mongoose.Schema;

const adminSchema = new Schema({
    eid: {
        type: String,
        required: true,
    },
    name: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    gender: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    phone: {
        type: String,
        required: true,
    },

})

export default mongoose.model("Admin", adminSchema);

